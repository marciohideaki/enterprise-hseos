/**
 * Commit Message Validator Tests
 *
 * Validates that scripts/governance/validate-commit-msg.sh correctly
 * accepts valid conventional commits and rejects forbidden patterns.
 */

const path = require('node:path');
const { execSync } = require('node:child_process');

const REPO_ROOT = path.join(__dirname, '..');
const VALIDATOR = 'scripts/governance/validate-commit-msg.sh';

function runValidator(msg) {
  try {
    execSync(`"${VALIDATOR}" "${msg}"`, { cwd: REPO_ROOT, stdio: 'pipe', shell: true });
    return { exitCode: 0 };
  } catch (error) {
    return { exitCode: error.status ?? 1 };
  }
}

const cases = [
  {
    description: 'valid conventional commit — fix with scope',
    msg: 'fix(cli): remove broken method',
    expectPass: true,
  },
  {
    description: 'valid conventional commit — feat with scope',
    msg: 'feat(tools): add glob support',
    expectPass: true,
  },
  {
    description: 'no type prefix — plain sentence',
    msg: 'Remove broken method',
    expectPass: false,
  },
  {
    description: 'AI mention in subject',
    msg: 'fix(cli): remove method generated by claude',
    expectPass: false,
  },
  {
    description: 'forbidden Co-Authored-By trailer',
    msg: 'fix(cli): remove method\n\nCo-Authored-By: Someone <x@y.com>',
    expectPass: false,
  },
  {
    description: 'subject longer than 100 characters',
    msg: `fix(cli): ${'x'.repeat(101)}`,
    expectPass: false,
  },
];

let passed = 0;
let failed = 0;

for (const tc of cases) {
  const { exitCode } = runValidator(tc.msg);
  const succeeded = exitCode === 0;
  const ok = succeeded === tc.expectPass;

  if (ok) {
    console.log(`  PASS  ${tc.description}`);
    passed++;
  } else {
    const got = succeeded ? 'exit 0 (pass)' : `exit ${exitCode} (fail)`;
    const want = tc.expectPass ? 'exit 0 (pass)' : 'exit 1 (fail)';
    console.error(`  FAIL  ${tc.description} — got ${got}, want ${want}`);
    failed++;
  }
}

console.log(`\nCommit message validator tests: ${passed} passed, ${failed} failed`);

if (failed > 0) {
  process.exit(1);
}
